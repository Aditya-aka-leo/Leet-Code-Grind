class Solution {
  public:
    int equalSum(int N, vector<int> &nums) {
    int total=0;
       for(auto i:nums)
       total+=i;
       int i=0;
       int j=total;
       for(int m=0;m<nums.size();m++)
       {
           j=j-nums[m];
           if(i==j)
           return m+1;
           i=i+nums[m];
       }
       return -1;
    }
};


